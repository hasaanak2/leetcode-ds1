/**
 * print the first 100 terms in the Fibonacci sequence staring with 0.0 You will need to use a double if the output is in scientific notation or it has
 * decimals. Output: 0.0, 1.0, 1.0, 2.0, 3.0, 5.0, 8.0 and so on...
 */

public class FibonacciSequence {

    public static void main(String[] args) {
        double a = 0.0;
        double b = 1.0;

        System.out.print(a + ", " + b + ", ");

        for(int i = 2; i < 100; i++){
            double c = a + b;
            System.out.print(c + ", ");

            a = b;
            b = c;
        }
    }
}
